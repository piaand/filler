#include "includes/libft.h"

void	ft_putnbr(int n)
{
	long i;

	i = n;
	if (i < 0)
	{
		ft_putchar('-');
		i *= -1;
	}
	if (i > 9)
	{
		ft_putnbr(i / 10);
		ft_putchar('0' + (i % 10));
	}
	else
	{
		ft_putchar('0' + i);
	}
}
